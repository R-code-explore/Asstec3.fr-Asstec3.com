        <h3 class="sub_info_subtitle">Routage SDA par SDA</h3>
        <p>Vous définissez numéro par numéro, les renvois d’appels que vous désirez établir. Vous paramétrez des règles de <u>renvoi permanent</u>, de renvoi sur non réponse et surtout de renvoi sur non connexion, très utile en cas de défaillance de lien ou de l’équipement PBX. Vous pouvez également définir des sonneries simultanées pour chacun des numéros : ainsi, vous paramétrez un renvoi sur un numéro hors Trunk.
        <br>
        Ce premier pas vers la <u>convergence</u> permet d’associer votre numéro de téléphone fixe à votre numéro de mobile.</p>
