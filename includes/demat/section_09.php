        <h3 class="sub_info_subtitle">Renforcez la valeur légale de vos documents</h3>
        <p>
        ASSTEC3 s’appuie sur un spécialiste français, expert de la signature électronique depuis 20 ans, pour vous proposer un outil qui respecte des conditions et des normes fixées et contrôlées par les autorités européennes et françaises.<br>
Cela nous permet de vous apporter toutes les garanties de sécurité et de conformité, pour conférer une valeur probante à vos documents signés en cas de litiges ou de contentieux.<br>
Sysnea e-Signature inclut les critères principaux que doit respecter une signature électronique :<br>
<strong>Une double authentification de la personne qui signe:</strong> le signataire est authentifié à la fois par son e-mail et son numéro de téléphone portable grâce à un code à usage unique (OTP, One Time Password) ;
<br><br>
<strong>- L’expression renforcée du consentement du signataire</strong> avec des textes personnalisables et des cases à cocher tracées dans le fichier de preuve ;<br><strong>- La garantie de l’intégrité du document</strong>, c’est-à-dire que son contenu soit figé dans le temps. L’horodatage de l’offre Sysnea e-Signature est qualifié eIDAS, ce qui garantit l’intégrité du document à la date et à l’heure de la signature depuis une source de temps fiable.<br>
<strong>- Une conservation à valeur probante pérenne du document signé</strong>, est proposée en standard pour garantir l’intégrité du document et la fiabilité des éléments de la signature pendant toute la durée de stockage.
