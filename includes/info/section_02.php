        <h3 class="sub_info_subtitle">Un bilan objectif de l'état de votre parc informatique</h3>
        <p>Lors de la première phase de l’audit, nous réalisons un état des lieux qui a pour but de fournir un diagnostic objectif de l’état de votre système informatique.<br>A la fois technique et fonctionnel, l’audit donne une photographie très nette, à l’instant T, du niveau de fiabilité et des capacités d’évolution de votre informatique.<br><br>
Notre méthodologie, adaptée aux petites et aux grandes structures, comporte plusieurs niveaux :<br>
• Vérification de l’infrastructure du parc informatique : systèmes, réseaux et matériels ;<br>• Contrôle de la configuration des postes, serveurs et messageries ;<br>• Analyse de la sécurité physique du système, des solutions de sauvegarde et de sécurité des données ;<br>• Capacités d’évolution et gestion des changements.
